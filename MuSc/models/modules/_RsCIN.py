def MMO(W, score, k_list=None):
    """
    多矩阵优化(MMO)函数，用于根据权重矩阵W和分数score计算优化后的分数。

    参数:
        W (torch.Tensor): 权重矩阵，形状为(n, n)
        score (torch.Tensor): 初始分数向量，形状为(n,)
        k_list (list): 需要尝试的不同k值的列表，默认为[1, 2, 3]

    返回:
        torch.Tensor: 优化后的分数向量，形状为(n,)
    """
    if k_list is None:
        k_list = [1, 2, 3]
    S_list = []  # 用于存储不同k值计算得到的分数向量
    for k in k_list:
        # 获取每行最小的(n-k)个元素的索引
        _, topk_matrix = torch.topk(W.float(), W.shape[0] - k, largest=False, sorted=True)
        # 创建权重掩码矩阵，将最小的(n-k)个元素置为0
        W_mask = W.clone()
        for i in range(W.shape[0]):
            W_mask[i, topk_matrix[i]] = 0
        # 创建度矩阵D_，其对角线元素为对应行权重和的倒数
        n = W.shape[-1]
        D_ = torch.zeros_like(W).float()
        for i in range(n):
            D_[i, i] = 1 / (W_mask[i, :].sum())
        # 计算转移矩阵P
        P = D_ @ W_mask
        # 初始化分数向量并应用转移矩阵
        S = score.clone().unsqueeze(-1)
        S = P @ S
        # 将当前k值计算得到的分数添加到列表中
        S_list.append(S)
    # 将不同k值计算得到的分数取平均，得到最终结果
    S = torch.concat(S_list, -1).mean(-1)
    return S


def RsCIN(scores_old, cls_tokens=None, k_list=[0]):
    """
    RsCIN函数实现了一种基于类别令牌相似性的分数调整方法

    参数:
        scores_old: 原始分数数组
        cls_tokens: 类别令牌向量，用于计算相似性矩阵
        k_list: 整数列表，指定相似性调整的参数k值，当包含0时直接返回原始分数

    返回:
        调整后的分数数组

    处理流程:
    1. 如果cls_tokens为None或k_list包含0，直接返回原始分数
    2. 将cls_tokens转换为numpy数组
    3. 对原始分数进行归一化处理
    4. 计算类别令牌之间的余弦相似性矩阵
    5. 使用MMO函数基于相似性矩阵调整分数
    6. 将结果转换回numpy数组并返回
    """
    if cls_tokens is None or 0 in k_list:  # 检查是否需要直接返回原始分数
        return scores_old
    cls_tokens = np.array(cls_tokens)  # 转换为numpy数组
    scores = (scores_old - scores_old.min()) / (scores_old.max() - scores_old.min())  # 归一化处理
    similarity_matrix = cls_tokens @ cls_tokens.T  # 计算相似性矩阵
    similarity_matrix = torch.tensor(similarity_matrix)  # 转换为torch张量
    scores_new = MMO(similarity_matrix.clone().float(), score=torch.tensor(scores).clone().float(),
                     k_list=k_list)  # 调用MMO函数调整分数
    scores_new = scores_new.numpy()  # 转换回numpy数组
    return scores_new  # 返回调整后的分数
